    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        conexion cc = new conexion();
        Connection cn = null;

        try {
            cn = cc.conectar();
            if (cn == null) {
                JOptionPane.showMessageDialog(this, "No se pudo establecer la conexi贸n con la base de datos.", "Error de Conexi贸n", JOptionPane.ERROR_MESSAGE);
                return;
            }

            String rutaReporte = "/reportes/ReporteXCedula.jrxml";
            String cedulaIngresada = JOptionPane.showInputDialog(this, "Ingrese la c茅dula:");

            if (cedulaIngresada == null || cedulaIngresada.trim().isEmpty()) {
                return; // si cancelan o dejan vac铆o
            }

            Map<String, Object> parametros = new HashMap<>();
            parametros.put("cedula", cedulaIngresada);

            // Compilar el reporte desde .jrxml
            java.io.InputStream reporteStream = getClass().getResourceAsStream(rutaReporte);
            if (reporteStream == null) {
                JOptionPane.showMessageDialog(this, "No se pudo encontrar el archivo de reporte: " + rutaReporte, "Error de Reporte", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            JasperReport reporte = JasperCompileManager.compileReport(reporteStream);
            JasperPrint imprimir = JasperFillManager.fillReport(reporte, parametros, cn);

            if (imprimir.getPages().isEmpty()) {
                JOptionPane.showMessageDialog(this, "No se encontraron resultados para la c茅dula ingresada.", "Reporte Vac铆o", JOptionPane.WARNING_MESSAGE);
                return;
            }

            //  Mostrar dentro del escritorio
            mostrarReporteInterno(imprimir, "Reporte por C茅dula");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurri贸 un error al generar el reporte:\n" + e.getMessage(), "Error de Reporte", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        } finally {
            try {
                if (cn != null && !cn.isClosed()) {
                    cn.close();
                }
            } catch (Exception e) {
                System.err.println("Error al cerrar la conexi贸n: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jMenuItem3ActionPerformed

